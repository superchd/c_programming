#include <unistd.h>

void	ft_putnbr(int nb)
{
	char n;

	n = '0';
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	else if (nb < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-nb);
	}
	else
	{
		if (nb > 9)
		{
			ft_putnbr(nb / 10);
		}
		n = nb % 10 + '0';
		write(1, &n, 1);
	}
}
